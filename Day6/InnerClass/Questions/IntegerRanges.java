package Day6.InnerClass.Questions;

public class IntegerRanges
{
    public static void main(String[] args) {
        System.out.println("Integer Max Value: "+ Integer.MAX_VALUE);//2^31 - 1
        System.out.println("Integer Min Value: "+ Integer.MIN_VALUE);//2^31 - 1
        System.out.println("Integer Max Value + 1: "+ (Integer.MAX_VALUE+1));//2^31 - 1
        System.out.println("Integer Min Value - 1: "+ (Integer.MIN_VALUE - 1));//2^31 - 1

    }
}
